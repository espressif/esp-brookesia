wifi_ap_config_t get_ap_config(const SoftApParams &ap_params)
{
    BROOKESIA_LOG_TRACE_GUARD();

    BROOKESIA_LOGD("Params: ap_params(%1%)", BROOKESIA_DESCRIBE_TO_STR(ap_params));

    // Initialize the config with default values
#pragma GCC diagnostic push
#pragma GCC diagnostic ignored "-Wmissing-field-initializers"
    wifi_ap_config_t cfg = {
        .ssid = "",
        .password = "",
        .channel = static_cast<uint8_t>(ap_params.has_channel() ? ap_params.get_channel() :
                                        BROOKESIA_SERVICE_WIFI_SOFTAP_CHANNEL_DEFAULT),
        .authmode = WIFI_AUTH_OPEN,
        .max_connection = ap_params.max_connection,
    };
#pragma GCC diagnostic pop

    if (!ap_params.ssid.empty()) {
        snprintf(reinterpret_cast<char *>(cfg.ssid), sizeof(cfg.ssid), "%s", ap_params.ssid.c_str());
    } else {
        auto default_ssid = SoftAp::get_ap_default_ssid();
        BROOKESIA_LOGW("No SSID provided, use default SSID: %1%", default_ssid);
        snprintf(reinterpret_cast<char *>(cfg.ssid), sizeof(cfg.ssid), "%s", default_ssid.c_str());
    }
    if (!ap_params.password.empty()) {
        snprintf(reinterpret_cast<char *>(cfg.password), sizeof(cfg.password), "%s", ap_params.password.c_str());
        cfg.authmode = WIFI_AUTH_WPA2_PSK;
    }

    return cfg;
}

